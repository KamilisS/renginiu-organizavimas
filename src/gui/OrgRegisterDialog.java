    private ArrayList<String> checkErrors() {
        ArrayList<String> errors = new ArrayList<>();

        String regex = "^[a-zA-Z]+$";
        String emailRegex = "(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])";
        
        if (!this.nameField.getText().matches(regex)) {
            errors.add("Blogai įvestas vardas!");
        }
        
        if (!this.surnameField.getText().matches(regex)) {
            errors.add("Blogai įvesta pavardė!");
        }
        
        if (!this.emailField.getText().matches(emailRegex)) {
            errors.add("Blogai įvestas el. paštas!");
        }
        
        if (!this.pswField.getText().matches(regex)) {
            errors.add("Blogai įvestas slaptažodis!");
        }
        
        if (!this.repeatPswField.getText().matches(regex)) {
            errors.add("Blogai įvestas pakartotas slaptažodis!");
        }
        
        if (!errors.contains("Blogai įvestas slaptažodis!") && !errors.contains("Blogai įvestas pakartotas slaptažodis!")) {
            if (!this.pswField.getText().equals(this.repeatPswField.getText())) {
                errors.add("Slaptažodžiai nesutampa!");
            }
        }
        
        if (this.bankAccField.getText().length() != 20) {
            errors.add("Banko sąskaitą turi sudaryti 20 simbolių!");
        }
        
        try {
            long id = Long.parseLong(this.peronalNumField.getText());
            if (id < 10000000000L || id > 99999999999L) {
                errors.add("Asmens kodą turi sudaryti 11 skaičių!");
            }
        } catch (NumberFormatException e) {
            errors.add("Blogai įvestas asmens kodas!");
        }
        
        if (this.emailField.getText().matches(emailRegex)
                && !(errors.contains("Asmens kodą turi sudaryti 11 skaičių!") || errors.contains("Blogai įvestas asmens kodas!"))
                && new MSSQL().doesOrganiserExist(this.emailField.getText(), Long.parseLong(this.peronalNumField.getText()))) {
            errors.add("Organizatorius su šiuo el. paštu arba asmens kodu jau egzistuoja!");
        }

        return errors;
    }

