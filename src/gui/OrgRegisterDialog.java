    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        String strDate = null;
        if (this.birthdayField.getDate() != null) {
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");  
            strDate = dateFormat.format(this.birthdayField.getDate());
        }
        
        ArrayList<String> errors = this.checkErrors();
        
        if (errors.size() > 0) {
            String errorMessage = "";
            for (String o : errors) {
                errorMessage += o + "\n";
            }
            
            JOptionPane.showMessageDialog(null,
                errorMessage,
                "Klaida(-os) formoje",
                JOptionPane.ERROR_MESSAGE);
        } else {
            boolean created = new MSSQL().createNewOrganiser(this.nameField.getText(),
                    this.surnameField.getText(),
                    this.emailField.getText(),
                    this.pswField.getText(),
                    this.bankAccField.getText(),
                    Long.parseLong(this.peronalNumField.getText()),
                    strDate
            );
            if (created) {
                this.dispose();
                JOptionPane.showMessageDialog(null,
                "Dabar galite prisijungti kaip organizatorius",
                "Organizatorius sukurtas!",
                JOptionPane.PLAIN_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null,
                "Nežinoma klaida!",
                null,
                JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_registerBtnActionPerformed


